    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        int returnVal = filePicker.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = filePicker.getSelectedFile();
            // What to do with the file, e.g. display it in a TextArea
            txtDirectory.setText(file.getAbsolutePath());
        } else {
            System.out.println("Eleccion de archivo cancelada.");
        }
    }//GEN-LAST:event_btnBrowseActionPerformed

    private void btnDecryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecryptActionPerformed
        AESHelper cifrador = new AESHelper(txtDirectory.getText(), txtKey.getText());
        if(cifrador.decrypt()){
            JOptionPane.showMessageDialog(this, "Archivo descifrado en 'texto_d.txt'.", "Exito", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Error al descifrar archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDecryptActionPerformed

    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        AESHelper cifrador = new AESHelper(txtDirectory.getText(), txtKey.getText());
        if(cifrador.encrypt()){
            JOptionPane.showMessageDialog(this, "Archivo cifrado en 'texto_c.txt'.", "Exito", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Error al cifrar archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

